class DoWhile {
    public static void main(String[] args) {
        int num = 0, num1 = 8;
        System.out.println("\nDo-while");
        do {
            System.out.println("Count is: " + num);
            num++;
        } while (num <= 8);
        System.out.println("\nDo-while");
        do {
            System.out.println("Count is: " + num1);
            num1++;
        } while (num1 <= 8);
    }
}