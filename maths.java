public class maths {
    public static void main(String[] args)
    {
        /**
         * Java Math class provides several methods 
         * to do mathematical operations and calculations.
         */
        System.out.println("\nMath class in java");
        System.out.println("Average: " + Math.abs(4.56));
        System.out.println("Min: " + Math.min(12,23));
        System.out.println("Max: " + Math.max(0, -1));
        System.out.println("Sqrt: " + Math.sqrt(70));
        System.out.println("Sqrt: " + (int) Math.sqrt(81)); // int makes the float to int;
        System.out.println();
    }
}
