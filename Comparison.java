class Comparion {
    public static void main(String[] args)
    {
        int myAge = 21;
        int brothersAge = 19;

        System.out.println("Comparison Operator: ");
        System.out.println(myAge > brothersAge);
        System.out.println(myAge < brothersAge);
        System.out.println(myAge == brothersAge);
        System.out.println(myAge != brothersAge);
        System.out.println(myAge >= brothersAge);
        System.out.println(myAge <= brothersAge);
    }
}