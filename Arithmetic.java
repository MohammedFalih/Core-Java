public class Arithmetic {
    public static void main(String[] args) {
        int num1 = 40;
        int num2 = 20;
        float num3 = 23.44f;
        /**
         * Arithmethic Operators in Java --> + , - , * , / , % 
         */
        System.out.println("\nArithmetic Operators");
        System.out.println("Addition: " + 2+6);
        System.out.println("Subtraction: " + (num1 - num2));
        System.out.println("Multiplication: " + (num1 * num3));
        System.out.println("Division: " + (num2 / 10));
        System.out.println("Modulus: " + (num1 % num3));
    }
}
